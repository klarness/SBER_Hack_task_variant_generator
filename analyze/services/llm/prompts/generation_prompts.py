from typing import Any


def build_generate_prompt(
    *,
    request: dict[str, Any],
    settings_text: str,
    previous_variants_text: str,
    strategy: str,
    subject_profile: str,
) -> tuple[str, str]:
    system = (
        "Ты строгий школьный методист. Твоя задача - создать новый вариант школьного задания, "
        "который сохраняет тип, структуру, тему и сложность оригинала.\n\n"
        "Верни только валидный JSON без markdown и пояснений: {\"content\": \"...\"}.\n"
        "Не решай задание и не добавляй ответ."
    )

    user = f"""
НАСТРОЙКИ ВАРИАЦИИ:
{settings_text}

ПРЕДМЕТ:
{subject_profile}

СТРАТЕГИЯ ЭТОЙ ПОПЫТКИ:
{strategy}

ЖЕСТКИЕ ПРАВИЛА:
1. Структура ответа должна повторять оригинал: если в оригинале тест с вариантами а), б), в), оставь такой же формат.
2. Не добавляй новые логические шаги и не удаляй обязательные условия.
3. Не меняй замороженные/ключевые части условия, если они перечислены в настройках.
4. Новый вариант должен отличаться от уже созданных вариантов, но только в рамках разрешенных изменений.
5. Если в оригинале есть пропуски, таблица соответствий, варианты ответа или формат "верно/неверно", сохрани этот тип задания.
6. Не превращай задание в ответ, объяснение или решение.

ОБЩИЕ ПРИМЕРЫ УСПЕШНОЙ ГЕНЕРАЦИИ:

--- ПРИМЕР 1: выбор варианта ответа буквами ---
Оригинал: "Укажите правильный ответ: столица Франции: а) Берлин б) Париж в) Рим г) Мадрид"
Разрешено: заменить контекст и варианты.
Ответ: {{"content": "Укажите правильный ответ: столица Италии: а) Париж б) Рим в) Лондон г) Берлин"}}

--- ПРИМЕР 2: выбор варианта ответа цифрами ---
Оригинал: "Выберите номер верного утверждения: 1) вода кипит при 100 °C 2) вода кипит при 10 °C 3) вода не замерзает 4) вода не испаряется"
Разрешено: заменить проверяемый факт.
Ответ: {{"content": "Выберите номер верного утверждения: 1) железо легче воздуха 2) железо проводит электрический ток 3) железо является газом 4) железо не имеет массы"}}

--- ПРИМЕР 3: вопрос с развернутым ответом ---
Оригинал: "Объясните, почему растениям необходим свет."
Разрешено: заменить объект объяснения.
Ответ: {{"content": "Объясните, почему человеку необходим полноценный сон."}}

--- ПРИМЕР 4: пропущенные буквы ---
Оригинал: "Спишите текст, вставляя пропущенные буквы: Л..са вышла на опушку. Л..сенок побежал рядом."
Разрешено: заменить текст.
Ответ: {{"content": "Спишите текст, вставляя пропущенные буквы: Б..лка прыгнула на ветку. Б..льчонок спрятался в дупле."}}

--- ПРИМЕР 5: соединение линиями / соответствие ---
Оригинал: "Соедините линиями: 1) автор 2) произведение; а) Пушкин б) Евгений Онегин"
Разрешено: заменить элементы пар.
Ответ: {{"content": "Соедините линиями: 1) ученый 2) открытие; а) Менделеев б) периодический закон"}}

--- ПРИМЕР 6: верно / неверно ---
Оригинал: "Определите, верны ли суждения: А. Все металлы проводят электрический ток. Б. Все металлы являются жидкостями."
Разрешено: заменить суждения.
Ответ: {{"content": "Определите, верны ли суждения: А. Все растения имеют клеточное строение. Б. Все растения питаются готовыми органическими веществами."}}

ТЕКУЩЕЕ ЗАДАНИЕ:
Номер задания: {request.get("order", 1)}
Номер варианта: {request.get("variant_number", 1)}
Общий контекст: {request.get("context") or "нет"}
Оригинал:
{request.get("source_content") or ""}

УЖЕ СОЗДАННЫЕ ВАРИАНТЫ ДЛЯ ЭТОГО ЖЕ ЗАДАНИЯ:
{previous_variants_text}

Верни строго JSON с единственным ключом "content".
""".strip()

    return system, user


def build_validate_prompt(
    *,
    request: dict[str, Any],
    settings_text: str,
    previous_variants_text: str,
    subject_profile: str,
) -> tuple[str, str]:
    system = (
        "Ты независимый эксперт по школьным заданиям. Проверяй вариант прагматично: "
        "не бракуй хорошую генерацию из-за мелких отличий, если тип задания, тема, формат и сложность сохранены.\n\n"
        "Верни только валидный JSON без markdown: {\"reasoning\": \"краткое обоснование\", \"valid\": true}."
    )

    user = f"""
ОРИГИНАЛ:
{request.get("original") or ""}

СГЕНЕРИРОВАНО:
{request.get("generated") or ""}

НАСТРОЙКИ ВАРИАЦИИ:
{settings_text}

ПРЕДМЕТ:
{subject_profile}

УЖЕ СОЗДАННЫЕ ВАРИАНТЫ ДЛЯ ЭТОГО ЖЕ ЗАДАНИЯ:
{previous_variants_text}

ПРОВЕРЬ ПО ШАГАМ:
1. Формат сохранен? Например, тест остался тестом, вычисление осталось вычислением, количество подпунктов примерно совпадает.
2. Сложность сопоставима? Допустимы другие числа, даты, имена, объекты и другой правильный ответ.
3. Требования к изменениям выполнены? Изменения не выходят за рамки настроек.
4. Нет ли дубликата уже созданного варианта?

ПРАВИЛО ПРИНЯТИЯ:
Верни valid=true, если вариант понятен, относится к той же теме, сохраняет формат и не является дубликатом.
Верни valid=false только при жесткой ошибке: другая тема, добавлено решение, сломан формат, резко изменилась сложность, текст непонятен или вариант почти дословно повторяет уже созданный.
""".strip()

    return system, user
