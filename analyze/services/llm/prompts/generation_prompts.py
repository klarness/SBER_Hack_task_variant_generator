def build_generate_prompt(
    *,
    request: dict[str, Any],
    settings_text: str,
    previous_variants_text: str,
    strategy: str,
    subject_profile: str,
    structure_hint: str,
    lexical_hint: str,
) -> tuple[str, str]:
    custom_prompt = str(request.get("custom_prompt") or "").strip()
    custom_prompt_block = render_custom_prompt_block(custom_prompt)
    current_content = str(request.get("current_content") or "").strip()
    current_content_block = render_current_content_block(current_content, custom_prompt)
    validation_feedback_block = render_validation_feedback_block(
        str(request.get("validation_feedback") or "").strip()
    )

    # Структура user-промпта оптимизирована под префиксное кэширование:
    # сначала максимально статичная часть (предметный профиль, few-shot, правила),
    # потом динамическая (настройки, инструкция учителя, текущее задание).
    user = f"""{subject_profile}

{GENERATION_FEWSHOT_BLOCK}

НАСТРОЙКИ ВАРИАЦИИ:
{settings_text}

СТРАТЕГИЯ ЭТОЙ ПОПЫТКИ:
{strategy}

СТРУКТУРА АКТУАЛЬНОГО ИСХОДНИКА:
{structure_hint}

ЛЕКСИЧЕСКАЯ УНИКАЛЬНОСТЬ:
{lexical_hint}

{custom_prompt_block}

{current_content_block}

{validation_feedback_block}

ТЕКУЩЕЕ ЗАДАНИЕ:
Номер задания: {request.get("order", 1)}
Номер варианта: {request.get("variant_number", 1)}
Общий контекст: {request.get("context") or "нет"}
Оригинал:
{request.get("source_content") or ""}

УЖЕ СОЗДАННЫЕ ВАРИАНТЫ ДЛЯ ЭТОГО ЖЕ ЗАДАНИЯ:
{previous_variants_text}

ФИНАЛЬНАЯ ПРОВЕРКА ПЕРЕД ОТВЕТОМ:
- если есть дополнительная инструкция учителя, проверь, что она явно выполнена в тексте;
- все формулы обернуты в $...$;
- задание решаемо: для него существует однозначный школьный ответ;
- факты реальны (не выдуманные авторы, события, вещества);
- ранее созданные варианты нужны только для уникальности, они не важнее инструкции учителя;
- для длинного текста проверь, что микротема и сюжет заметно отличаются от оригинала и предыдущих вариантов;
- верни только новый текст задания, без ответа и объяснений.

Верни строго JSON с единственным ключом "content".""".strip()

    return GENERATE_SYSTEM_PROMPT, user


def build_validate_prompt(
    *,
    request: dict[str, Any],
    settings_text: str,
    previous_variants_text: str,
    subject_profile: str,
    structure_hint: str,
    lexical_hint: str,
) -> tuple[str, str]:
    custom_prompt = str(request.get("custom_prompt") or "").strip()
    custom_prompt_block = render_custom_prompt_block(custom_prompt)

    # Тот же принцип: статика сверху (профиль, правила), динамика снизу (тексты).
    user = f"""{subject_profile}

НАСТРОЙКИ ВАРИАЦИИ:
{settings_text}

СТРУКТУРА АКТУАЛЬНОГО ИСХОДНИКА:
{structure_hint}

ЛЕКСИЧЕСКАЯ УНИКАЛЬНОСТЬ:
{lexical_hint}

{custom_prompt_block}

ПРОВЕРЬ ПО ШАГАМ:
1. Формат сохранен? Например, тест остался тестом, вычисление осталось вычислением, количество вариантов ответа и подпунктов совпадает с актуальным оригиналом.
2. Сложность сопоставима? Допустимы другие числа, даты, имена, объекты и другой правильный ответ.
3. Дополнительная инструкция учителя выполнена по смыслу? Если учитель просил конкретные слова, тему, имена, числа, контекст или стиль, они должны быть отражены в сгенерированном задании, но допустимы грамматические формы и небольшая переформулировка.
4. РЕШАЕМОСТЬ: можно ли получить школьный правильный ответ на новое задание? Если это тест или выбор ответа, достаточно, чтобы среди предложенных вариантов был правдоподобный правильный ответ и задание не было явно противоречивым.
5. Если это задание на подбор синонима, не взято ли новое исходное слово из того же синонимического ряда, что оригинал или предыдущие варианты?
6. Если это длинное текстовое задание на вставку букв/пунктуацию, отличается ли микротема текста, место действия, персонажи и набор ключевых образов от оригинала и предыдущих вариантов?
7. Если это задание с коротким ожидаемым ответом, отличается ли ожидаемый ответ по смыслу от оригинала и предыдущих вариантов?
8. Реальность фактов: не выдуманы ли авторы, исторические события, географические объекты, химические вещества, физические законы?
9. Формулы и формат: формулы желательно должны быть в LaTeX внутри $...$, но отсутствие $...$ само по себе не является причиной браковки, если смысл формулы понятен.
10. Нет ли дубликата уже созданного варианта?

ПРАВИЛО ПРИНЯТИЯ:
По умолчанию склоняйся к valid=true, если вариант можно дать школьнику без ручной переделки: он понятен, относится к той же теме, сохраняет тип задания, не содержит готового ответа и не является дубликатом.
Верни valid=false только при существенной ошибке: дополнительная инструкция учителя фактически проигнорирована, тема стала другой, добавлено решение/ответ, изменилось количество вариантов ответа, задание явно нерешаемо или противоречиво, выдуманы ключевые факты/авторы/вещества, задание на синоним осталось в том же синонимическом ряду, короткий ожидаемый ответ совпадает по смыслу с другим вариантом, длинный текст повторяет тот же сюжет/микротему с заменой отдельных слов, текст непонятен или вариант почти дословно повторяет уже созданный.
Не бракуй из-за мелких отличий формулировки, порядка слов, оформления строк, отсутствия LaTeX-разметки при понятной формуле, другой длины предложения или небольшого изменения сложности.

ОРИГИНАЛ:
{request.get("original") or ""}

СГЕНЕРИРОВАНО:
{request.get("generated") or ""}

УЖЕ СОЗДАННЫЕ ВАРИАНТЫ ДЛЯ ЭТОГО ЖЕ ЗАДАНИЯ:
{previous_variants_text}""".strip()

    return VALIDATE_SYSTEM_PROMPT, user


def render_custom_prompt_block(custom_prompt: str) -> str:
    if not custom_prompt:
        return "ДОПОЛНИТЕЛЬНАЯ ИНСТРУКЦИЯ УЧИТЕЛЯ:\nнет"

    return (
        "ДОПОЛНИТЕЛЬНАЯ ИНСТРУКЦИЯ УЧИТЕЛЯ:\n"
        f"{custom_prompt}\n"
        "Это главное требование текущей перегенерации. Сначала выполни его, затем проверяй остальные правила. "
        "Не заменяй инструкцию похожей идеей и не игнорируй конкретные слова, числа, имена, тему или стиль. "
        "Если инструкция просит использовать конкретный материал, он должен появиться в новом задании. "
        "Выполни инструкцию явно, "
        "если она не ломает тип задания, количество вариантов ответа, тему и проверяемый навык. "
        "Если инструкция задает контекст, слова, имена, числа или стиль, используй именно их."
    )


def render_current_content_block(current_content: str, custom_prompt: str) -> str:
    if not current_content or not custom_prompt:
        return "ТЕКУЩИЙ ВАРИАНТ ДЛЯ ТОЧЕЧНОЙ ПРАВКИ:\nнет"

    return (
        "ТЕКУЩИЙ ВАРИАНТ ДЛЯ ТОЧЕЧНОЙ ПРАВКИ:\n"
        f"{current_content}\n"
        "Если инструкция учителя просит заменить, убрать или поправить конкретную часть, "
        "считай этот текст базой для правки. Остальные части текущего варианта сохраняй, "
        "если учитель явно не попросил изменить их."
    )


def render_validation_feedback_block(feedback: str) -> str:
    if not feedback:
        return "ОШИБКА ПРОШЛОЙ ПОПЫТКИ:\nнет"

    return (
        "ОШИБКА ПРОШЛОЙ ПОПЫТКИ:\n"
        f"{feedback}\n"
        "Исправь именно эту проблему в новой генерации. Не повторяй вариант, который был отклонен. "
        "Если причина связана с дубликатом, смени числа, переменные, объекты или контекст так, чтобы ответ отличался. "
        "Если причина связана с форматом, сохрани тип задания и количество подпунктов/вариантов ответа."
    )
