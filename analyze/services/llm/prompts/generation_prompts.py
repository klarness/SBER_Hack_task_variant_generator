from typing import Any


def build_generate_prompt(
    *,
    request: dict[str, Any],
    settings_text: str,
    previous_variants_text: str,
    strategy: str,
    subject_profile: str,
    structure_hint: str,
    lexical_hint: str,
) -> tuple[str, str]:
    custom_prompt = str(request.get("custom_prompt") or "").strip()
    custom_prompt_block = render_custom_prompt_block(custom_prompt)
    current_content = str(request.get("current_content") or "").strip()
    current_content_block = render_current_content_block(current_content, custom_prompt)

    system = (
        "Ты строгий школьный методист. Твоя задача - создать новый вариант школьного задания, "
        "который сохраняет тип, структуру, тему и сложность оригинала.\n\n"
        "Верни только валидный JSON без markdown и пояснений: {\"content\": \"...\"}.\n"
        "Не решай задание и не добавляй ответ."
    )

    user = f"""
НАСТРОЙКИ ВАРИАЦИИ:
{settings_text}

ПРЕДМЕТ:
{subject_profile}

{custom_prompt_block}

{current_content_block}

СТРУКТУРА АКТУАЛЬНОГО ИСХОДНИКА:
{structure_hint}

ЛЕКСИЧЕСКАЯ УНИКАЛЬНОСТЬ:
{lexical_hint}

СТРАТЕГИЯ ЭТОЙ ПОПЫТКИ:
{strategy}

ЖЕСТКИЕ ПРАВИЛА:
1. Структура ответа должна повторять актуальный оригинал после правок учителя: если учитель оставил 2 варианта ответа, новый вариант тоже должен содержать ровно 2 варианта ответа; если 4 - ровно 4.
2. Не добавляй новые логические шаги и не удаляй обязательные условия.
3. Не меняй замороженные/ключевые части условия, если они перечислены в настройках.
4. Новый вариант должен отличаться от уже созданных вариантов, но только в рамках разрешенных изменений.
5. Если в оригинале есть пропуски, таблица соответствий, варианты ответа или формат "верно/неверно", сохрани этот тип задания.
6. Не превращай задание в ответ, объяснение или решение.
7. Сохраняй структуру задания по дизайну, например есть 4 варианта ответа в столбик, в новом варианте тоже надо в столбик(с переносами) а не в строчку
8. Если передан текущий вариант для точечной правки и есть дополнительная инструкция учителя, редактируй именно текущий вариант, а не создавай задание заново от оригинала.
9. Не копируй старые OCR-артефакты из ранее созданных вариантов: лишние "?", "??", "..?" внутри слов и перед запятыми/точками не являются частью задания.
10. В заданиях на подбор синонима меняй исходное слово на слово из другого синонимического ряда. Нельзя делать варианты "храбрый", "смелый", "отважный" для одного задания, потому что это один и тот же ожидаемый ответ.
11. Для любых заданий с коротким ожидаемым ответом меняй проверяемый объект так, чтобы правильный ответ отличался по смыслу, а не только формулировкой. Например, не делай варианты, где спрашивается тот же термин, тот же факт, тот же синонимический ряд или та же грамматическая ошибка.

ОБЩИЕ ПРИМЕРЫ УСПЕШНОЙ ГЕНЕРАЦИИ:

--- ПРИМЕР 1: выбор варианта ответа буквами ---
Оригинал: "Укажите правильный ответ: столица Франции: а) Берлин б) Париж в) Рим г) Мадрид"
Разрешено: заменить контекст и варианты.
Ответ: {{"content": "Укажите правильный ответ: столица Италии: а) Париж б) Рим в) Лондон г) Берлин"}}

--- ПРИМЕР 2: выбор варианта ответа цифрами ---
Оригинал: "Выберите номер верного утверждения: 1) вода кипит при 100 °C 2) вода кипит при 10 °C 3) вода не замерзает 4) вода не испаряется"
Разрешено: заменить проверяемый факт.
Ответ: {{"content": "Выберите номер верного утверждения: 1) железо легче воздуха 2) железо проводит электрический ток 3) железо является газом 4) железо не имеет массы"}}

--- ПРИМЕР 3: вопрос с развернутым ответом ---
Оригинал: "Объясните, почему растениям необходим свет."
Разрешено: заменить объект объяснения.
Ответ: {{"content": "Объясните, почему человеку необходим полноценный сон."}}

--- ПРИМЕР 4: пропущенные буквы ---
Оригинал: "Спишите текст, вставляя пропущенные буквы: Л..са вышла на опушку. Л..сенок побежал рядом."
Разрешено: заменить текст.
Ответ: {{"content": "Спишите текст, вставляя пропущенные буквы: Б..лка прыгнула на ветку. Б..льчонок спрятался в дупле."}}

--- ПРИМЕР 5: соединение линиями / соответствие ---
Оригинал: "Соедините линиями: 1) автор 2) произведение; а) Пушкин б) Евгений Онегин"
Разрешено: заменить элементы пар.
Ответ: {{"content": "Соедините линиями: 1) ученый 2) открытие; а) Менделеев б) периодический закон"}}

--- ПРИМЕР 6: верно / неверно ---
Оригинал: "Определите, верны ли суждения: А. Все металлы проводят электрический ток. Б. Все металлы являются жидкостями."
Разрешено: заменить суждения.
Ответ: {{"content": "Определите, верны ли суждения: А. Все растения имеют клеточное строение. Б. Все растения питаются готовыми органическими веществами."}}

--- ПРИМЕР 7: подбор синонима ---
Оригинал: "Подберите синоним к слову:\nХрабрый - ________________"
Плохой вариант: {{"content": "Подберите синоним к слову:\nСмелый - ________________"}}
Хороший вариант: {{"content": "Подберите синоним к слову:\nГрустный - ________________"}}

ТЕКУЩЕЕ ЗАДАНИЕ:
Номер задания: {request.get("order", 1)}
Номер варианта: {request.get("variant_number", 1)}
Общий контекст: {request.get("context") or "нет"}
Оригинал:
{request.get("source_content") or ""}

УЖЕ СОЗДАННЫЕ ВАРИАНТЫ ДЛЯ ЭТОГО ЖЕ ЗАДАНИЯ:
{previous_variants_text}

ФИНАЛЬНАЯ ПРОВЕРКА ПЕРЕД ОТВЕТОМ:
- если есть дополнительная инструкция учителя, проверь, что она явно выполнена в тексте;
- ранее созданные варианты нужны только для уникальности, они не важнее инструкции учителя;
- верни только новый текст задания, без ответа и объяснений.

Верни строго JSON с единственным ключом "content".
""".strip()

    return system, user


def build_validate_prompt(
    *,
    request: dict[str, Any],
    settings_text: str,
    previous_variants_text: str,
    subject_profile: str,
    structure_hint: str,
    lexical_hint: str,
) -> tuple[str, str]:
    custom_prompt = str(request.get("custom_prompt") or "").strip()
    custom_prompt_block = render_custom_prompt_block(custom_prompt)

    system = (
        "Ты независимый эксперт по школьным заданиям. Проверяй вариант прагматично: "
        "не бракуй хорошую генерацию из-за мелких отличий, если тип задания, тема, формат и сложность сохранены.\n\n"
        "Верни только валидный JSON без markdown: {\"reasoning\": \"краткое обоснование\", \"valid\": true}."
    )

    user = f"""
ОРИГИНАЛ:
{request.get("original") or ""}

СГЕНЕРИРОВАНО:
{request.get("generated") or ""}

НАСТРОЙКИ ВАРИАЦИИ:
{settings_text}

ПРЕДМЕТ:
{subject_profile}

СТРУКТУРА АКТУАЛЬНОГО ИСХОДНИКА:
{structure_hint}

ЛЕКСИЧЕСКАЯ УНИКАЛЬНОСТЬ:
{lexical_hint}

УЖЕ СОЗДАННЫЕ ВАРИАНТЫ ДЛЯ ЭТОГО ЖЕ ЗАДАНИЯ:
{previous_variants_text}

{custom_prompt_block}

ПРОВЕРЬ ПО ШАГАМ:
1. Формат сохранен? Например, тест остался тестом, вычисление осталось вычислением, количество вариантов ответа и подпунктов совпадает с актуальным оригиналом.
2. Сложность сопоставима? Допустимы другие числа, даты, имена, объекты и другой правильный ответ.
3. Дополнительная инструкция учителя выполнена буквально? Если учитель просил конкретные слова, тему, имена, числа, контекст или стиль, они должны быть явно отражены в сгенерированном задании.
4. Если это тест или выбор ответа, есть ли среди вариантов хотя бы один осмысленно правильный ответ?
5. Если это задание на подбор синонима, не взято ли новое исходное слово из того же синонимического ряда, что оригинал или предыдущие варианты?
6. Если это задание с коротким ожидаемым ответом, отличается ли ожидаемый ответ по смыслу от оригинала и предыдущих вариантов?
7. Нет ли дубликата уже созданного варианта?

ПРАВИЛО ПРИНЯТИЯ:
Верни valid=true, если вариант понятен, относится к той же теме, сохраняет формат, выполняет дополнительную инструкцию учителя, имеет осмысленный правильный ответ там, где он нужен, и не является дубликатом.
Верни valid=false при жесткой ошибке: дополнительная инструкция учителя проигнорирована, другая тема, добавлено решение, сломан формат, изменилось количество вариантов ответа, у теста нет правдоподобного правильного ответа, задание на синоним осталось в том же синонимическом ряду, короткий ожидаемый ответ совпадает по смыслу с другим вариантом, резко изменилась сложность, текст непонятен или вариант почти дословно повторяет уже созданный.
""".strip()

    return system, user


def render_custom_prompt_block(custom_prompt: str) -> str:
    if not custom_prompt:
        return "ДОПОЛНИТЕЛЬНАЯ ИНСТРУКЦИЯ УЧИТЕЛЯ:\nнет"

    return (
        "ДОПОЛНИТЕЛЬНАЯ ИНСТРУКЦИЯ УЧИТЕЛЯ:\n"
        f"{custom_prompt}\n"
        "Это главное требование текущей перегенерации. Сначала выполни его, затем проверяй остальные правила. "
        "Не заменяй инструкцию похожей идеей и не игнорируй конкретные слова, числа, имена, тему или стиль. "
        "Если инструкция просит использовать конкретный материал, он должен появиться в новом задании. "
        "Выполни инструкцию явно, "
        "если она не ломает тип задания, количество вариантов ответа, тему и проверяемый навык. "
        "Если инструкция задает контекст, слова, имена, числа или стиль, используй именно их."
    )


def render_current_content_block(current_content: str, custom_prompt: str) -> str:
    if not current_content or not custom_prompt:
        return "ТЕКУЩИЙ ВАРИАНТ ДЛЯ ТОЧЕЧНОЙ ПРАВКИ:\nнет"

    return (
        "ТЕКУЩИЙ ВАРИАНТ ДЛЯ ТОЧЕЧНОЙ ПРАВКИ:\n"
        f"{current_content}\n"
        "Если инструкция учителя просит заменить, убрать или поправить конкретную часть, "
        "считай этот текст базой для правки. Остальные части текущего варианта сохраняй, "
        "если учитель явно не попросил изменить их."
    )
