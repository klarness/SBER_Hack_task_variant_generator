def subject_prompt(subject: str | None) -> str:
    normalized = (subject or "").strip()
    if not normalized:
        return "Предметный профиль не выбран. Используй общие правила генерации."
    return SUBJECT_PROMPT_SCAFFOLDS.get(
        normalized,
        f"Предметный профиль: {normalized}. TODO: описать специальные правила предмета.",
    )
